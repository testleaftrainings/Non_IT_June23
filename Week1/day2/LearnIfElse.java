package week1.day2;

public class LearnIfElse {

	public static void main(String[] args) {
		int num=1;
		//-5>0
		if(num>2) {
			System.out.println("positive number");
		}
		if(num>=1) {
			System.out.println("number ");
		}
		
		else if(num==0) {
			System.out.println("neutral");
		}
		
		else {
			System.out.println("negative number");
		}
	}

}
